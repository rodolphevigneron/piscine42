int		ft_atoi(const char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		while (str[i] > 47 && str[i] < 58)
		{
			i++;
		}
	}
	int	tab_r[i];
	while (str[j])
	{
		while (str[j] > 47 && str[j] < 58)
		{
			tab_r[j] = str[j];
			j++;
		}
	}
	return (*tab_r);
}
