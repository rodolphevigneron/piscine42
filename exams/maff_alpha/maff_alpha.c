#include <unistd.h>

int		main(void)
{
	char	min;
	char	maj;

	min = 'a';
	maj = 'B';
	while (maj <= 'Z')
	{
		while (min <= 'y')
		{
			write(1, &min, 1);
			write(1, &maj, 1);
			min = min + 2;
			maj = maj + 2;
		}
	}
	write(1, "\n", 1);
}
