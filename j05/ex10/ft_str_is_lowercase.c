int		ft_str_is_lowercase(char *str)
{
	int		i;
	int		j;

	i = -1;
	j = 0;
	while (str[++i])
	{
		if (str[i] > 96 && str[i] < 123)
			j++;
	}
	if (!(*str) || i == j)
		return (1);
	else
		return (0);
}
