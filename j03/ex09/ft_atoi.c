int	ft_atoi(char *str)
{
	int		i;
	int		j;
	int		ret;

	i = 0;
	j = 1;
	ret = 0;
	while ((str[i] != '\0')
			&& ((str[i] >= '0' && str[i] <= '9')
			|| str[i] == '-'
			|| str[i] == '+'))
		i++;
	i--;
	while (i >= 0)
	{
		ret += (str[i] - '0') * j;
		i--;
		j = j * 10;
	}
	return (ret);
}
