int		ft_any(char **tab, int (*f)(char*))
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (tab[j])
	{
		if (((*f)(tab[j])) == 1)
			i++;
		j++;
	}
	if (i > 0)
		return (1);
	else
		return (0);
}
