int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < length)
	{
		if (((*f)(tab[i], tab[i + 1])) < 0 || ((*f)(tab[i], tab[i + 1]) == 0))
			j++;
		i++;
	}
	if (j == length)
		return (1);
	else
		return (0);
}
