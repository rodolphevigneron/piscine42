#include "data.h"
#include "func.h"

int		ft_sqrt(int nb)
{
	int		i;

	i = 1;
	while ((i * i != nb) && i < (nb / 2))
	{
		i++;
	}
	if ((i * i) == nb)
		return (i);
	else
		return (0);
}
